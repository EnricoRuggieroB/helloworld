#Algoritmo che dato in input il lato di un cubo, ne calcola il volume.

lato = input("Inserire il lato del cubo. ") #Input del lato del cubo
lato = int(lato) #Cast al tipo "int" (intero)
volume = lato * lato * lato #Calcolo del volume del cubo
print("Il volume del cubo di lato",lato,"e':",volume) #Stampa del volume del cubo